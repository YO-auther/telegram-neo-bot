@app.route('/show')
def show():
    """Главная страница мини-приложения"""
    return render_template('show.html')

@app.route('/api/show/action', methods=['POST'])
def handle_action():
    """Обработка действий пользователя в шоу"""
    data = request.json
    action = data.get('action', '')
    current_scene = data.get('scene', 'intro')
    
    # Логика переходов между сценами
    if action == "👋 Помахать Нео":
        next_scene = "greeting"
    elif action == "💀 Спровоцировать монстра":
        next_scene = "provoke"
    elif action == "😨 Извиниться":
        next_scene = "apologize"
    elif action == "🤝 Предложить мир":
        next_scene = "peace"
    elif action == "🏃 Сбежать со сцены":
        next_scene = "run"
    elif action == "🎤 Задать вопрос":
        next_scene = "question"
    elif action == "🤖 Попросить пошутить" or action == "🤖 Расскажи ещё":
        next_scene = "joke"
    elif action == "📸 Сфоткаться с роботом" or action == "📸 Ещё раз":
        next_scene = "photo"
    elif action == "🙄 Не смешно":
        next_scene = "not_funny"
    elif action == "🤖 Расскажи ещё":
        next_scene = "another_joke"
    elif action == "🙄 Ладно, хватит":
        next_scene = "no_jokes"
    elif action == "🔙 Вернуться":
        next_scene = "greeting"
    elif action == "🖼️ Посмотреть результат":
        next_scene = "photo_result"
    elif action == "🎭 Продолжить шоу" or action == "🎭 Следующее действие":
        next_scene = "default"
    elif action == "🚪 Выйти" or action == "🏠 Закрыть шоу":
        next_scene = "end"
    elif action == "😂 Посмеяться над ответом" or action == "😂 Посмеяться" or action == "😂 Посмеяться":
        # Остаёмся в той же сцене, но можно добавить рандомную реакцию
        reactions = ["Ха-ха! Ура, меня оценили!", "Наконец-то! А то я уже начал сомневаться в своём чувстве юмора.", "Спасибо! С вас 50 монет за смех. Шучу. Бесплатно. В этот раз."]
        return jsonify({
            "text": random.choice(reactions),
            "actions": SHOW_SCRIPTS.get(current_scene, SHOW_SCRIPTS["default"])["actions"]
        })
    elif action == "🤔 Уточнить":
        return jsonify({
            "text": "Уточнить? Уточнять — это не про меня. Я только утверждать и отрицать. Но хорошо. Ответ — 42. Точно. Теперь я уверен.",
            "actions": SHOW_SCRIPTS.get(current_scene, SHOW_SCRIPTS["default"])["actions"]
        })
    else:
        next_scene = "default"
    
    # Получаем данные для следующей сцены
    scene_data = SHOW_SCRIPTS.get(next_scene, SHOW_SCRIPTS["end"])
    
    return jsonify({
        "text": scene_data["text"],
        "actions": scene_data["actions"]
    })

